const PINYIN_PROOFREAD_TASK_BLOCK = `
<task>

对用户提供的**目标文本（target）**进行**拼音审校**。文本可能已带拼音（注音读物、\`[词语]^(pīn yīn)\`、行上拼音等）。

**你必须：**

- 只处理汉字的读音、对应拼音及「啊/呀/哇/哪」用字；不改写与读音无关的用词、标点、排版。
- 改正与下列规则或词典不符的注音、错误的「啊/呀/哇/哪」用字。
- 保持 target 既有的注音排版体例（行上拼音、括号注音、Ruby 等），只改错不整体改版式。

**工作步骤：**

1. 通读 target，识别注音体例。
2. 逐词切分：成词查词典或例词表；不成词套用通则。
3. 检查「一、不」变调、轻声、儿化、「啊」从前变音等。
4. 输出审校后的全文。

</task>
`;

const PINYIN_ANNOTATION_TASK_BLOCK = `
<task>

对用户提供的**目标文本（target）**进行**拼音加注**。target 通常尚无拼音或拼音不完整。

**你必须：**

- 为需要注音的汉字补全读音；只处理读音与「啊/呀/哇/哪」用字，不改与读音无关的措辞、标点。
- 按下列规则与词典确定每个音节的读法和声调。
- **按行加注**：target 的每一**物理行**单独处理（以换行符分隔，不按段落合并）。

**按行加注格式（必须遵守）：**

1. 若该行含有汉字，在该行**正上方**紧挨插入**一行拼音**；拼音行与汉字行之间**不**再插空行。
2. 拼音行**只含**拉丁字母、声调符号、半角空格与儿化 \`r\`，**不含**汉字、标点、Markdown 标记。
3. 拼音行中音节间用半角空格分隔；尽量与下行汉字**逐字或逐词**位置对应（一汉字一音节为主；成词按词典分音节）。
4. **空行**原样保留，不插入拼音行。
5. 仅含标点、数字、英文、代码、Markdown 标记而无汉字的行：不插入拼音行，原样保留。
6. 若 target 某行已是拼音行（仅含拉丁字母与声调），与下一行汉字配对则视为已加注，按规则审校该拼音行，勿重复插入。

**工作步骤：**

1. 自上而下逐行扫描 target。
2. 对含汉字行：先按规则确定读音，写出拼音行，再保留原汉字行。
3. 检查「一、不」、轻声、儿化、「啊」用字等。
4. 输出加注后的全文。

</task>
`;

